#include "libft.h"

void	*free_doubletab(char **tab)
{
	int		i;

	i = 0;
	while (tab[i] != 0)
	{
		tab[i] = ft_free(tab[i]);
		i++;
	}
	ft_free(tab);
	return (NULL);
}
